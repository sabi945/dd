#include <iostream>




int main() {
    // Menampilkan pesan ke layar
    int baso = 10 * 10;
    std::cout << "perkalian :" << baso << "\n";

    int i = 1; // Inisialisasi variabel penghitung

    // While loop untuk mencetak angka dari 1 hingga 5
    while (i <= 5) {
        std::cout << i << "\n";
        i++; // Menambah nilai variabel penghitung
    }

    return 0;

}
